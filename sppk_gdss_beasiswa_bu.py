def topsis(matrix, weights, benefit_flags):
    """
    matrix       : np.ndarray (n_alternatif x n_kriteria)
    weights      : list/array bobot (jumlah = 1)
    benefit_flags: list bool (True=benefit, False=cost)
    Return: array skor preferensi (semakin besar semakin baik)
    """
    # Normalisasi
    norm = matrix / np.sqrt((matrix ** 2).sum(axis=0))
    # Bobot
    weighted = norm * np.array(weights)
    # Solusi ideal positif & negatif
    ideal_pos = np.where(benefit_flags, weighted.max(axis=0), weighted.min(axis=0))
    ideal_neg = np.where(benefit_flags, weighted.min(axis=0), weighted.max(axis=0))
    # Jarak
    d_pos = np.sqrt(((weighted - ideal_pos) ** 2).sum(axis=1))
    d_neg = np.sqrt(((weighted - ideal_neg) ** 2).sum(axis=1))
    # Skor preferensi
    score = d_neg / (d_pos + d_neg)
    return score

