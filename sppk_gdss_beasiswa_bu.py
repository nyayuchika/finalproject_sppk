# ─── GDSS BORDA ─────────────────────────────────────────────
def borda_aggregation(scores_list, ids):
    """
    scores_list : list of np.array skor dari setiap DM
    ids         : list ID alternatif
    Return      : DataFrame dengan Borda Score dan ranking akhir
    """
    n = len(ids)
    rankings = []
    for sc in scores_list:
        rank = pd.Series(sc).rank(ascending=False, method="min").values.astype(int)
        borda_pts = n - rank   # poin borda = n - rank
        rankings.append(borda_pts)

    borda_total = np.sum(rankings, axis=0)
    df_borda = pd.DataFrame({
        "ID"            : ids,
        "Borda_DM1_Esai": rankings[0],
        "Borda_DM2_RS"  : rankings[1],
        "Borda_DM3_Waw" : rankings[2],
        "Borda_Total"   : borda_total,
    })
    df_borda["Ranking_GDSS"] = df_borda["Borda_Total"].rank(ascending=False, method="min").astype(int)
    df_borda = df_borda.sort_values("Ranking_GDSS").reset_index(drop=True)
    return df_borda

