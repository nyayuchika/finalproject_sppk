def cek_administrasi(row):
    issues = []
    flags  = {}

    # 1. Usia
    usia_ok = (row["Usia"] < 32) or (row["Usia"] <= 33 and row["Status_Kuliah"] == "Aktif")
    flags["Usia_OK"] = usia_ok
    if not usia_ok:
        issues.append("⛔ Usia tidak memenuhi syarat")

    # 2. Rekomendasi
    rek_ok = row["Ada_Rekomendasi"] and row["File_Rek_Valid"]
    flags["Rekomendasi_OK"] = rek_ok
    if not row["Ada_Rekomendasi"]:
        issues.append("⛔ Surat rekomendasi tidak ada")
    elif not row["File_Rek_Valid"]:
        issues.append("🔴 File rekomendasi perlu verifikasi manual")

    # 3. LoA / Surat Aktif
    loa_ok = row["Ada_LoA_Aktif"]
    flags["LoA_OK"] = loa_ok
    if not loa_ok:
        issues.append("⛔ Surat LoA/Surat Aktif tidak ada")

    # 4. IPK
    ipk_ok = row["IPK"] >= 3.00
    flags["IPK_OK"] = ipk_ok
    if not ipk_ok:
        issues.append(f"⛔ IPK {row['IPK']} < 3.00")

    # 5. UKBI
    ukbi_ok = row["UKBI"] >= 578
    flags["UKBI_OK"] = ukbi_ok
    if not ukbi_ok:
        issues.append(f"⛔ UKBI {row['UKBI']} < 578 (min: Unggul)")

    # 6. Bahasa Inggris (skor PTE >= 58 sebagai patokan umum)
    eng_ok = row["Skor_Bahasa_Ing"] >= 58
    flags["BahasaIng_OK"] = eng_ok
    if not eng_ok:
        issues.append(f"⛔ Skor Bahasa Inggris {row['Skor_Bahasa_Ing']} tidak memenuhi syarat")

    # 7. Rencana Studi
    rs_ok = row["Ada_Rencana_Studi"] and row["File_RS_Valid"]
    flags["RencanaStudi_OK"] = rs_ok
    if not row["Ada_Rencana_Studi"]:
        issues.append("⛔ Rencana studi tidak ada")
    elif not row["File_RS_Valid"]:
        issues.append("🔴 File rencana studi perlu verifikasi manual")

    # 8. Esai
    esai_ok = row["Ada_Esai"] and row["File_Esai_Valid"]
    flags["Esai_OK"] = esai_ok
    if not row["Ada_Esai"]:
        issues.append("⛔ Esai tidak ada")
    elif not row["File_Esai_Valid"]:
        issues.append("🔴 File esai perlu verifikasi manual")

    all_ok        = all(flags.values())
    butuh_manusia = any("🔴" in i for i in issues)
    status        = "LULUS" if all_ok else ("VERIFIKASI_MANUAL" if butuh_manusia else "GUGUR")

    return pd.Series({
        **flags,
        "Isu"            : "; ".join(issues) if issues else "✅ Semua persyaratan terpenuhi",
        "Butuh_Manual"   : butuh_manusia,
        "Status_Admin"   : status,
    })

